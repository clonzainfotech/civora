      <p>A pregnancy test can let you know if you are pregnant. Here are answers to some common questions about them.</p>
      <h4>What is a pregnancy test, and how does it work?</h4>
      <p>Pregnancy tests check your pee or blood for a hormone called human chorionic gonadotropin (hCG). Your body makes this hormone after a fertilized egg attaches to the wall of your uterus.</p>
      <p>This usually happens about 6 days after fertilization. Levels of hCG rise quickly, doubling every 2 to 3 days.</p>
      <h4>What types of pregnancy tests are available?</h4>
      <p>Two main types of pregnancy tests are blood tests and urine tests.</p>
      <h4>Blood tests</h4>
      <p>You get these at your doctor's office, but they’re not used as often as urine tests. These tests can detect pregnancy earlier than a home pregnancy test, about 6 to 8 days after ovulation. It takes longer to get the results than with a home pregnancy test.</p>
      <p>The two types of blood pregnancy tests are:</p>
      <p>A qualitative hCG test simply checks for hCG. It gives a "yes" or "no" answer to the question, "Are you pregnant?" Doctors often order these tests to confirm pregnancy as early as 10 days after conception. Some can detect hCG much earlier.</p>
      <p>A quantitative hCG test (beta hCG) measures the exact amount of hCG in your blood. It can find even very low levels of hCG. These tests may help track problems during pregnancy. Your doctor may use them along with other tests to rule out an ectopic pregnancy, when the fertilized egg implants outside your uterus, or after a miscarriage, when hCG levels fall quickly.</p>
      <h4>Urine tests</h4>
      <p>You can take these at home or in a doctor's office.</p>
      <p>Along with being private and convenient, home pregnancy tests are quick and easy to use. They’re also very accurate if you follow the directions. These pregnancy tests all work in a similar way. You test your pee in one of these ways:</p>
      <p> - Hold the test stick in your urine stream</p>
      <p> - Collect pee in a cup and dip the test stick into it</p>
      <p> - Collect pee in a cup and use a dropper to put it into another container</p>
      <p>You’ll need to wait a few minutes before seeing the results.</p>
      <p>After you take this test, you can confirm your results by seeing your doctor, who can do even more sensitive pregnancy tests.</p>
      <h4>How accurate are pregnancy tests?</h4>
      <p>Urine home pregnancy tests are about 99% accurate. Blood tests are even more accurate.</p>
      <p>A home test’s accuracy depends on:</p>
      <p> - How closely you follow the instructions</p>
      <p> - When you ovulate and how soon the egg implants</p>
      <p> - How soon after pregnancy you take the test</p>
      <p> - The sensitivity of the pregnancy test</p>
      <h4>When to Take a Pregnancy Test</h4>
      <p>Some pregnancy tests can spot hCG before you miss a period. But the results will be more accurate if you wait until the first day of a missed period.</p>
      <p>Results may also be more accurate if you do the test first thing in the morning, when your urine is more concentrated.</p>
      <h4>Where can I get a home pregnancy test?</h4>
      <p>You can buy a home pregnancy test in a drugstore without a prescription. The cost depends on the brand. But most tests aren’t very expensive.</p>
      <h4>What do the pregnancy test results mean?</h4>
      <p>Results may show up as a line, a color, or a symbol such as a "+" or "-" sign. Digital tests show the words "pregnant" or "not pregnant." It's important to know what a positive or negative result means.</p>
      <p>If you get a positive result, you’re pregnant. This is true no matter how faint the line, color, or sign is. If you get a positive result, you may want to call your doctor to talk about what comes next.</p>
      <p>In very rare cases, you can have a false-positive result. This means you're not pregnant but the test says you are. You could have a false-positive result if you have blood or protein in your pee. Certain drugs, such as tranquilizers, anticonvulsants, hypnotics, and fertility drugs, could cause false-positive results.</p>
      <p>If you get a negative result, you’re probably not pregnant. But you may be pregnant if:</p>
      <p> - The test is past its expiration date.</p>
      <p> - You took the test the wrong way.</p>
      <p> - You tested too soon.</p>
      <p> - Your pee is too diluted because you drank a lot of fluids right before the test.</p>
      <p> - You’re taking certain medications, such as diuretics or antihistamines.</p>
      <p>If you get a negative result, try retesting within about a week to double-check. Some home pregnancy tests suggest doing this no matter what your first results are.</p>
      <p>If you take the test twice and get different results, call your doctor. A blood test is a good idea to confirm results.</p>
      <p>If you have any other questions about the pregnancy test or the results, call your doctor or the telephone number listed with the test.</p>

