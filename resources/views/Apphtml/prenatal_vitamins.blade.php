      <h4>What Are Prenatal Vitamins?</h4>
      <p>Prenatal vitamins are supplements made for pregnant women to give their bodies the vitamins and minerals needed for a healthy pregnancy. Your doctor may suggest that you take them when you begin to plan for pregnancy, as well as while you’re pregnant.</p>
      <p>Eating a healthy diet is always a wise idea -- especially during pregnancy. It's also a good idea to take a prenatal vitamin to help cover any nutritional gaps in your diet.</p>
      <h4>What to Look for in Prenatal Vitamins</h4>
      <p>Prenatal vitamins help ensure that you get the nutrients you need for a healthy pregnancy.</p>
      <p>Look for prenatal vitamins that have:</p>
      <p> - 400 micrograms (mcg) of folic acid</p>
      <p> - 400 IU of vitamin D</p>
      <p> - 200 to 300 milligrams (mg) of calcium</p>
      <p> - 70 mg of vitamin C</p>
      <p> - 3 mg of thiamine</p>
      <p> - 2 mg of riboflavin</p>
      <p> - 20 mg of niacin</p>
      <p> - 6 mcg of vitamin B12</p>
      <p> - 10 mg of vitamin E</p>
      <p> - 15 mg of zinc</p>
      <p> - 17 mg of iron</p>
      <p> - 150 micrograms of iodine</p>
      <p>In some cases, your doctor will give you a prescription for a certain type of prenatal vitamin.</p>
      <p><b>Folic Acid, Calcium, Iodine, and Iron</b></p>
      <h4>Folic acid</h4>
      <p>If getting pregnant is a possibility for you, you should take folic acid. It can prevent birth defects that affect the baby’s brain and spinal cord. Neural tube defects develop early in pregnancy, before many women know they’re pregnant; half of all pregnancies are unplanned. This is why doctors recommend that any woman who could get pregnant take 400 micrograms (mcg) of folic acid daily, starting before conception and continuing for the first 12 weeks of pregnancy.</p>
      <p>If you’ve had a baby with a neural tube defect you should talk with your health care provider about folic acid. Studies have shown that taking a larger dose (up to 4,000 micrograms) at least one month before and during the first trimester may help if you’ve had a baby with this defect. But talk to your doctor about what’s right for you.</p>
      <p>Foods that have folic acid include:</p>
      <p> - Green leafy vegetables</p>
      <p> - Nuts</p>
      <p> - Beans</p>
      <p> - Citrus fruits</p>
      <p> - Many other foods fortified with folic acid</p>
      <p>If you’ve had a baby with a neural tube defect you should talk with your health care provider about folic acid. Studies have shown that taking a larger dose (up to 4,000 micrograms) at least one month before and during the first trimester may help if you’ve had a baby with this defect. But talk to your doctor about what’s right for you.</p>
      <h4>Calcium</h4>
      <p>Calcium is also important for a pregnant woman. It can help prevent you from losing your bone density as the baby uses calcium for its own bone growth.</p>
      <h4>Iodine</h4>
      <p>Iodine is critical for a woman’s healthy thyroid function during pregnancy. Iodine deficiency can cause:</p>
      <p> -  Miscarriage</p>
      <p> - Stillbirth</p>
      <p> - Stunted physical growth</p>
      <p> - Severe mental disability</p>
      <p> - Deafness</p>
      <h4>Iron</h4>
      <p>Iron helps your body make more blood red cells. These blood cells carry oxygen to the baby that it needs to develop.</p>
      <h4>What About Other Nutrients?</h4>
      <p>There are other nutrients that may improve the health of your pregnancy. Your doctor can help you decide if you need to take supplements that include:</p>
      <p><b>Omega-3 fatty acids: </b>These fats, which include DHA (docosahexaenoic acid) and EPA (eicosapentaenoic acid), come only from food sources such as fatty fish and nuts. Studies show omega-3s can lower your risk of preterm birth and of having a baby with low birth weight. If you don’t eat much food that’s rich in omega-3s, ask your health care provider if a supplement is right for you.</p>
      <p><b>Choline:</b> Although your body can make some choline on its own, you get most of it from food. Rich sources include beef, pork, chicken, fish, and eggs. Many pregnant women don’t get enough choline, which the baby needs for healthy brain growth.</p>
      <h4>When to Start Taking Prenatal Vitamins</h4>
      <p>The best time to start taking prenatal vitamins is before conception. Folic acid is especially important. You should begin taking a folic acid supplement at least 1 month before you try to get pregnant to prevent birth defects.</p>
      <p>Some doctors recommend that all women who could have a baby take prenatal vitamins, even if they don’t plan a pregnancy.</p>
      <h4>Prenatal Vitamin Side Effects</h4>
      <p>Some prenatal vitamins can cause nausea in an already nauseated pregnant woman. If that happens to you, talk to your health care provider. They may be able to prescribe a different kind of prenatal vitamin that you don’t have to swallow whole. Options include:</p>
      <p> - Chewables</p>
      <p> - Liquids</p>
      <p>The iron in prenatal vitamins may also make you constipated. If you’re constipated it can help to:</p>
      <p> - Eat a high-fiber diet</p>
      <p> - Drink lots of water</p>
      <p> - Exercise if your doctor says it’s safe for you</p>
      <p> - Take a stool softener with your doctor’s OK</p>

